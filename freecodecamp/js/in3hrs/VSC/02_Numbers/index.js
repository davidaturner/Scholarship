var a = 2;
var b = 5;

// addition
console.log(a + b);

// subtraction
console.log(a - b);

// multiplication
console.log(a * b);

// division
console.log(a / b);

// increment
console.log(a++);

// decrement
console.log(a--);

// remainder/modulo
console.log(a % b);

// shortcuts
a += b;
console.log(a);
a -= b;
console.log(a);
a *= b;
console.log(a);
a /= b;
console.log(a);

// decimals
console.log(2.3 + 5.5);
console.log(2.3 - 5.5);
console.log(2.3 * 5.5);
console.log(2.3 / 5.5);



