// Data types exercise 1
// String
var joe = "Joe";
console.log(joe);

// Number
var twentythree = 23;
console.log(twentythree);

// Array
var arr = ["Joe", 23, ["Joe", 23]];
console.log(arr);
console.log(arr[0]);
console.log(arr[1]);
console.log(arr[2]);
console.log(arr[2][0]);
console.log(arr[2][1]);

// Object
var object = {
    key: "value",
    funct: function () {
        return 'hey';
    }
};
console.log(object);
console.log(object.key);
console.log(object.funct());

// Boolean
var twoages = 23 > 47;
console.log(twoages);

// null
var none = null;
console.log(none);

// undefined
var under;
console.log(under);

