// declare your first name
var firstName = "Joe";

// console.log your first name
console.log(firstName);

// console.log capitalize first name.
console.log(firstName.toUpperCase());

// console.log first name all uppercase.
console.log(firstName.toUpperCase());

// console.log first name all lowercase.
console.log(firstName.toLowerCase());

// declare your last name
var lastName = "Garcia";

// console.log last name
console.log(lastName);

// console.log the concat of first name and last name.
console.log(firstName.concat(" ", lastName));