# math library
import math

Val = 3.14
sqrtVal = math.sqrt(Val)
print("{:2.2f}".format(sqrtVal))

expVal = math.exp(Val)
print(f"{expVal:2.4f}")

IntVal = 3
facVal = math.factorial(IntVal)
print(facVal)

sinVal = math.sin(Val)
print("{:.6f}".format(sinVal))

floorVal = math.floor(Val)
print(floorVal)

ceilVal = math.ceil(Val)
print(ceilVal)