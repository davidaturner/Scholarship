package _501;

public class Floating {

	public static void main(String[] args) {

		// single precision is 32 bits; double precision is 64 bits.
		float myMinFloatValue = Float.MIN_VALUE;
		float myMaxFloatValue = Float.MAX_VALUE;
		System.out.println("Float Minimum Value = " + myMinFloatValue);
		System.out.println("Float Maximum Value = " + myMinFloatValue);
		
		double myMinDoubleValue = Double.MIN_VALUE;
		double myMaxDoubleValue = Double.MAX_VALUE;
		System.out.println("Double Minimum Value = " + myMinFloatValue);
		System.out.println("Double Maximum Value = " + myMinFloatValue);
		
		int myIntValue = 5;
		float myFloatValue = 5.25f;
		double myDoubleValue = 5.25d;
	}

}
