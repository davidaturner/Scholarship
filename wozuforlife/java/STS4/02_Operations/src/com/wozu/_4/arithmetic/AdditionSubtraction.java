package com.wozu._4.arithmetic;

public class AdditionSubtraction {

	public static void main(String[] args) {
		
		additionExamples();
	}

	public static void additionExamples() {
		System.out.println(2+2);
		
		int sum = 2 + 2;
		System.out.println(sum);
		
		sum = 1 + 2 + 3 + 4 + 5;
		System.out.println(sum);
		
		sum = 4;

		// adding one to the variable
		sum = sum + 1;

		System.out.println(sum);
		
		sum = 4;

		// adding one to the variable
		sum++;

		System.out.println(sum);
		
		sum = 4;

		// subtracting one from the variable
		sum--;

		System.out.println(sum);

	}
}
