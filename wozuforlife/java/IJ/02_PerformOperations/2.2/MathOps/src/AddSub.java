public class AddSub {

    public static void main(String[] args) {

        System.out.println(2+2);

        int sum = 2 + 2;
        System.out.println(sum);

        sum = 4;

        // adding one to the variable
        sum = sum + 1;
        System.out.println(sum);

        sum = 4;

        // adding one to the variable
        sum++;
        System.out.println(sum);

        sum = 4;

        // subtracting one from the variable
        sum -= 1;

        System.out.println(sum);

        sum = 4;

        // subtracting one from the variable
        sum--;

        System.out.println(sum);
    }
}
