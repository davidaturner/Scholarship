// A String is a group of words, like a sentence. When writing a String in JavaScript, 
// you need to wrap the group of words you are using within quotes.
// A Number is just that, a number. Unlike a String, a Number does not need to be 
// wrapped in anything. If you write out a number, like 24, it will be read as a number 
// automatically.
// A Boolean is a logical entity and can only have two values: true and false. A Boolean 
// is like a Number in that it does not need to be wrapped in quotes. If you set something 
// to true without quotes, the value will be interpreted as Boolean.

//String
console.log("I am a sentence and in JavaScript, I am called a String");

//Number
console.log(24);

//Boolean
console.log(true);